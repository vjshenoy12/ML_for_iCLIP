#!/usr/bin/env python3
import argparse
import pandas as pd
import numpy as np

try:
    import matplotlib
    # matplotlib.use("Agg") # Commented out: 'Agg' backend prevents plots from showing in Colab output
    import matplotlib.pyplot as plt
except ImportError:
    raise ImportError("matplotlib not available")

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--activeCsv", default="/content/drive/MyDrive/Coursework/CHEMENG 277/Final Project/runs_active.csv")
    ap.add_argument("--out", default="active_learning_heatmap.png")
    # Pass an empty list to parse_args() to prevent it from trying to parse kernel-specific arguments
    args = ap.parse_args([])

    active = pd.read_csv(args.activeCsv)
    active_base = active[(active["pipeHeight_mm"] == 8.0) & (active["D"] == 1.88e9)].copy()

    # Pivot table: placement x Qtot
    pivot = active_base.pivot_table(index="placement", columns="Qtot_uLmin", values="N", aggfunc="count", fill_value=0)

    # Reorder
    placement_order = ["T", "S", "SZ", "TS"]
    pivot = pivot.reindex(placement_order)

    fig, ax = plt.subplots(figsize=(7, 4), dpi=150)
    im = ax.imshow(pivot.values, cmap="Blues", aspect="auto")

    ax.set_xticks(np.arange(len(pivot.columns)))
    ax.set_yticks(np.arange(len(pivot.index)))
    ax.set_xticklabels([int(c) for c in pivot.columns])
    ax.set_yticklabels(pivot.index)

    ax.set_xlabel("Flow rate (μL/min)", fontsize=11, weight='bold')
    ax.set_ylabel("Placement", fontsize=11, weight='bold')
    ax.set_title("Selection of candidate points\n(20 new points, baseline geometry)", fontsize=12, weight='bold')

    # Annotate counts
    for i in range(len(pivot.index)):
        for j in range(len(pivot.columns)):
            val = pivot.values[i, j]
            if not np.isnan(val) and val != 0: # Only annotate non-NaN and non-zero values
                text = ax.text(j, i, int(val), ha="center", va="center", color="black", fontsize=10)

    plt.colorbar(im, ax=ax, label="Count")
    plt.tight_layout()
    plt.savefig(args.out)
    plt.close()
    print(f"Saved {args.out}")

if __name__ == "__main__":
    main()

