# 8. Define the make_ridge_pipeline function
def make_ridge_pipeline(features):
    cat_cols = ["placement"] # Use original 'placement' column for OneHotEncoding
    num_cols = [c for c in features if c not in cat_cols]

    preprocessor = ColumnTransformer(
        transformers=[
            ("cat", OneHotEncoder(handle_unknown="ignore"), cat_cols),
            ("num", StandardScaler(), num_cols),
        ],
        remainder="drop"
    )
    alphas = np.logspace(-6, 6, 61)
    return Pipeline(steps=[
        ("pre", preprocessor),
        ("ridge", RidgeCV(alphas=alphas, cv=5))
    ])

# Prepare X_train and X_test using original 'placement' column for Ridge
