CASE_RE = re.compile(r"^(?P<runID>[A-Z]\d{2,3})_(?P<placement>T|S|SZ|TS)_N(?P<N>\d+)_Q(?P<Q>\d+)$")

def parse_case_name(name: str):
    m = CASE_RE.match(name)
    if not m:
        raise ValueError(f"Cannot parse case name: {name}")
    return {"runID": m.group("runID")}

def make_ridge_pipeline(feature_cols):
    cat_cols = [c for c in ["placement"] if c in feature_cols]
    num_cols = [c for c in feature_cols if c not in cat_cols]

    pre = ColumnTransformer(
        transformers=[
            ("cat", OneHotEncoder(handle_unknown="ignore"), cat_cols),
            ("num", StandardScaler(), num_cols),
        ],
        remainder="drop"
    )
    alphas = np.logspace(-6, 6, 61)
    return Pipeline(steps=[
        ("pre", pre),
        ("ridge", RidgeCV(alphas=alphas, cv=5))
    ])

def encode_categorical(df, cat_cols):
    encoders = {}
    for c in cat_cols:
        le = LabelEncoder()
        df[c] = le.fit_transform(df[c].astype(str))
        encoders[c] = le
    return encoders

def main():
    ap = argparse.ArgumentParser()
    # Set default to the user's specific file path based on prior interaction
    ap.add_argument("--combinedPredCsv", default="/content/drive/MyDrive/Coursework/CHEMENG 277/Final Project/oof_predictions_ridge.csv")
    ap.add_argument("--out", default="ridge_vs_gbm_comparison.png")
    ap.add_argument("--gbmEstimators", type=int, default=200)
    ap.add_argument("--gbmDepth", type=int, default=4)
    ap.add_argument("--gbmLr", type=float, default=0.05)
    # Pass an empty list to parse_args() to prevent it from trying to parse kernel-specific arguments
    args = ap.parse_args([])

    df = pd.read_csv(args.combinedPredCsv)

    feature_cols = ["pipeHeight_mm", "D", "placement", "N", "Qtot_uLmin"]
    cat_features = ["placement"]

    for c in feature_cols + ["deadFraction"]:
        if c not in df.columns:
            raise RuntimeError(f"Missing column: {c}")

    df = df.dropna(subset=feature_cols + ["deadFraction"]).copy()
    print(f"Training on {len(df)} rows")

    y_true = df["deadFraction"].values

    # --- Train Ridge ---
    X_ridge = df[feature_cols].copy()
    ridge_model = make_ridge_pipeline(feature_cols)
    ridge_model.fit(X_ridge, y_true)
    y_pred_ridge = ridge_model.predict(X_ridge)
    r2_ridge = r2_score(y_true, y_pred_ridge)
    print(f"Ridge R²: {r2_ridge:.3f}")

    # --- Train GBM ---
    df_gbm = df[feature_cols + ["deadFraction"]].copy()
    encode_categorical(df_gbm, cat_features)
    X_gbm = df_gbm[feature_cols].values
    y_gbm = df_gbm["deadFraction"].values

    gbm_model = GradientBoostingRegressor(
        n_estimators=args.gbmEstimators,
        max_depth=args.gbmDepth,
        learning_rate=args.gbmLr,
        random_state=0
    )
    gbm_model.fit(X_gbm, y_gbm)
    y_pred_gbm = gbm_model.predict(X_gbm)
    r2_gbm = r2_score(y_true, y_pred_gbm)
    print(f"GBM R²: {r2_gbm:.3f}")

    # --- Plot side-by-side ---
    lo = min(y_true.min(), y_pred_ridge.min(), y_pred_gbm.min())
    hi = max(y_true.max(), y_pred_ridge.max(), y_pred_gbm.max())
    pad = 0.02 * (hi - lo) if hi > lo else 0.01
    lo -= pad
    hi += pad

